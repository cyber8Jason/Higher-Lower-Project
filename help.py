
'''
Hauptmenü:

Im Hauptmenü kannst du aus verschiedenen Optionen wählen:
- Spiel starten: Beginne ein neues Spiel.
- Highscore anzeigen: Schau dir die besten Spieler und ihre Punktzahlen an.
- Hilfe anzeigen: Erhalte Informationen und Tipps zum Spiel.
- Verlassen: Beende das Spiel.

Spielmodi:

1.  Standard-Modus: Der klassische Spielmodus, bei dem du in jeder Runde entscheiden musst,
    welcher von zwei Artikeln häufiger aufgerufen wurde wobei der letzte immer übernommen wird.
    --> Drücke 1 oder 2 für den Artikel mit den MEISTEN Aufrufen.
2.  Random-Modus: Ein Modus, bei dem die Artikel zufällig ausgewählt werden, was das Spiel noch unvorhersehbarer macht.
3.  Zeit-Modus: Ein Modus, bei dem du gegen die Zeit spielst und schnell entscheiden musst.
4.  Geo-Modus: Ein Modus, der sich auf geografische Themen konzentriert.
    --> Drücke 1 oder 2 für den Artikel mit den MEISTEN Einwohnern/Fläche.
5.  Multiplayer-Modus: Spiele gegen Freunde und finde heraus, wer der bessere Spieler ist.
'''